package com.czaeshop.pojo;

public class NavigatorIcon {
    private String name; //导航图标名称
    private String iconURL; //icon路径
    private String navigatorURL; //跳转路径

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIconURL() {
        return iconURL;
    }

    public void setIconURL(String iconURL) {
        this.iconURL = iconURL;
    }

    public String getNavigatorURL() {
        return navigatorURL;
    }

    public void setNavigatorURL(String navigatorURL) {
        this.navigatorURL = navigatorURL;
    }
}
